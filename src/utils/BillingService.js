// BillingService.js
// Shared Supabase read/write service layer for the Billing Management module.
//
// MIGRATED FROM FIRESTORE. The big structural change: recordPayment() and
// markBalanceSettled() used to be Firestore runTransaction() calls (folio
// update + payment write + transaction-log write, all atomic). Supabase's
// JS client has no equivalent multi-write transaction primitive, so those
// two now call Postgres RPC functions (record_payment / mark_balance_settled
// — see the SQL migration) that do the same atomic multi-table update
// server-side. Every exported function here keeps its original name and
// camelCase return shape, so none of the 7 screens that import from this
// file needed to change.
//
// folioNumber/receiptNumber generation also simplified: the old
// getNextSequence() counter-transaction dance against a counters/billing
// doc is gone entirely — billing_records.folio_number and
// payments.receipt_number are auto-generated by native Postgres
// sequences (folio_seq/receipt_seq) as column DEFAULTs, so a plain
// INSERT already returns the assigned number.

import { supabase } from '../services/supabase';

// ---------------------------------------------------------------------------
// snake_case (DB) <-> camelCase (app) mapping
// ---------------------------------------------------------------------------

function billingRecordToCamel(row) {
  if (!row) return null;
  return {
    id: row.id,
    folioNumber: row.folio_number,
    reservationRef: row.reservation_id,
    guestUid: row.guest_user_id,
    guestName: row.guest_name,
    roomNumbers: row.room_numbers,
    checkInDate: row.check_in_date,
    checkOutDate: row.check_out_date,
    roomCharges: row.room_charges,
    additionalCharges: row.additional_charges,
    taxServiceCharges: row.tax_service_charges,
    totalAmountDue: row.total_amount_due,
    amountPaid: row.amount_paid,
    remainingBalance: row.remaining_balance,
    billingStatus: row.billing_status,
    createdAt: row.created_at,
    updatedAt: row.updated_at,
  };
}

function paymentToCamel(row) {
  if (!row) return null;
  return {
    id: row.id,
    folioId: row.folio_id,
    receiptNumber: row.receipt_number,
    reservationRef: row.reservation_id,
    guestName: row.guest_name,
    paymentDate: row.payment_date,
    paymentMethod: row.payment_method,
    amountPaid: row.amount_paid,
    remainingBalanceAfter: row.remaining_balance_after,
    processedByUid: row.processed_by,
    processedByName: row.processed_by_name,
  };
}

function transactionToCamel(row) {
  if (!row) return null;
  return {
    id: row.id,
    timestamp: row.timestamp,
    guestName: row.guest_name,
    reservationRef: row.reservation_id,
    paymentType: row.payment_type,
    amount: row.amount,
    staffUid: row.staff_id,
    staffName: row.staff_name,
    status: row.status,
    note: row.note,
  };
}

