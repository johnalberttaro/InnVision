/**
 * Records a payment against a folio, updates the folio's running totals
 * and billing status, generates a receipt, and writes a transaction log
 * entry — all atomically via the record_payment() Postgres function, the
 * replacement for the old Firestore runTransaction() block.
 */
export async function recordPayment({
  folioId,
  amount,
  paymentMethod, // "cash" | "gcash" | "card" | "pay_at_hotel"
  processedByUid,
  processedByName,
}) {
  if (amount <= 0) {
    throw new Error('Payment amount must be greater than zero.');
  }

  const { data, error } = await supabase.rpc('record_payment', {
    p_folio_id: folioId,
    p_amount: amount,
    p_payment_method: paymentMethod,
    p_processed_by: processedByUid || null,
    p_processed_by_name: processedByName,
  });
  if (error) throw error;

  return {
    receiptId: data.receiptId,
    receiptNumber: data.receiptNumber,
    transactionId: data.transactionId,
    newAmountPaid: data.newAmountPaid,
    newRemainingBalance: data.newRemainingBalance,
    newStatus: data.newStatus,
  };
}

